<?php
class ModelExtensionPaymentOgpay extends Model {
	public function getMethod($address, $total) {
		$this->load->language('extension/payment/ogpay');

			$status = true;
			
		if(empty($this->config->get('payment_ogpay_checkout_title'))){
		    $title = "Ogpay";
		}else{
		    $title = $this->config->get('payment_ogpay_checkout_title');
		}
	
		$method_data = array();

		if ($status) {
			$method_data = array(
				'code'       => 'ogpay',
				'title'      => $title,
				'terms'      => '',
				'sort_order' => $this->config->get('payment_ogpay_sort_order')
			);
		}

		return $method_data;
	}
	
	public function updateTrackID($track_id,$order_id){
		$this->db->query("UPDATE `" . DB_PREFIX . "order` SET track_id = '" . $this->db->escape($track_id) . "' WHERE order_id = '" . (int)$order_id . "'");
	}
	
	public function getOrder($order_id) {
		$order_query = $this->db->query("SELECT *, (SELECT os.name FROM `" . DB_PREFIX . "order_status` os WHERE os.order_status_id = o.order_status_id AND os.language_id = o.language_id) AS order_status FROM `" . DB_PREFIX . "order` o WHERE o.order_id = '" . (int)$order_id . "'");

		if ($order_query->num_rows) {
			$country_query = $this->db->query("SELECT * FROM `" . DB_PREFIX . "country` WHERE country_id = '" . (int)$order_query->row['payment_country_id'] . "'");

			if ($country_query->num_rows) {
				$payment_iso_code_2 = $country_query->row['iso_code_2'];
				$payment_iso_code_3 = $country_query->row['iso_code_3'];
			} else {
				$payment_iso_code_2 = '';
				$payment_iso_code_3 = '';
			}

			$zone_query = $this->db->query("SELECT * FROM `" . DB_PREFIX . "zone` WHERE zone_id = '" . (int)$order_query->row['payment_zone_id'] . "'");

			if ($zone_query->num_rows) {
				$payment_zone_code = $zone_query->row['code'];
			} else {
				$payment_zone_code = '';
			}

			$country_query = $this->db->query("SELECT * FROM `" . DB_PREFIX . "country` WHERE country_id = '" . (int)$order_query->row['shipping_country_id'] . "'");

			if ($country_query->num_rows) {
				$shipping_iso_code_2 = $country_query->row['iso_code_2'];
				$shipping_iso_code_3 = $country_query->row['iso_code_3'];
			} else {
				$shipping_iso_code_2 = '';
				$shipping_iso_code_3 = '';
			}

